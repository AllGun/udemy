// Atribuição simples
let num1 = 3;

// Exemplo de caso que eu precise reatribuir um valor
num1 = num1 + 20;
console.log(num1);

// Atribuição com operadores (reatribuição)
let num2 = 10;
console.log(num2);
//Soma
num2 += 3;
console.log(num2);

//Subtração
num2 -= 3;
console.log(num2);

//Multiplicação
num2 *= 5;
console.log(num2);

//Divisão
num2 /= 5;
console.log(num2);
