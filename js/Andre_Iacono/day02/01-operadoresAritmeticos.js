let num1 = 3;
let num2 = 2;
let num3 = 24;
let num4 = 33;
let num5 = 2;

// soma
console.log(num1 + num2);
//subtração
console.log(num1 - num2);
console.log(num1 * num2);
console.log(num3 / num2);

//Exponencial
console.log(num1 ** num2);

// Resto da divisão
console.log(num4 % num1);

console.log();
console.log("Incremento");
console.log(++num1); //incrementa e "imprime"
console.log(num1++); //"imprime" e incrementa
console.log(num1);

//Reduzir - decremento
console.log();
console.log("Decremento");
console.log(--num1); //decrementa e "imprime"
console.log(num1--); //"imprime" e incrementa
console.log(num1);
